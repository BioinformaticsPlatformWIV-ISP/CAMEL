import json
import logging
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Any

from camel.app.components.genedetection.genedetectionutils import GeneDetectionUtils


@dataclass
class GeneDetectionDBInfo:
    cluster_by_seq: Dict[str, str]
    seq_by_allele: Dict[str, str]
    metadata_by_seq: Dict[str, str]


class ValidationUtils(object):
    """
    This class contains utility function to help with the validation of tools / pipelines.
    """

    @staticmethod
    def parse_summary_out(path: Path) -> Dict[str, str]:
        """
        Parses the tabular summary output file generated by the pipeline.
        Warning: types are not converted (everything is returned as a string)
        :param path: Path to summary file
        :return: Summary information
        """
        logging.info(f"Parsing summary info: {path}")
        with open(str(path)) as handle:
            return {p[0]: p[1].strip() for p in [l.split('\t') for l in handle.readlines()]}

    @staticmethod
    def get_gene_detection_db_info(db_folder: Path) -> GeneDetectionDBInfo:
        """
        Parses the information from a gene detection database folder.
        :param db_folder: Database folder
        :return: Database information
        """
        clusters_by_seq = GeneDetectionUtils.parse_clusters(
            str(db_folder / f'{db_folder.name.lower()}-clustered_80.fasta'))
        with open(str(db_folder / 'mapping.txt')) as handle:
            mapping = json.load(handle)
        metadata_by_seq = {s: GeneDetectionUtils.parse_header(header)[-1] for s, header in mapping.items()}
        seq_by_allele = {metadata['allele']: seq for seq, metadata in metadata_by_seq.items()}
        return GeneDetectionDBInfo(clusters_by_seq, seq_by_allele, metadata_by_seq)

    @staticmethod
    def parse_gene_detection_output(dir_gene_detection_out: Path) -> List[Dict[str, Any]]:
        """
        Parses the output directory generated by the gene detection workflow and returns the detected hits as a list
        of dictionaries.
        :param dir_gene_detection_out: Output directory
        :return: List of detected genes
        """
        try:
            tsv_path = list(dir_gene_detection_out.glob('**/*.tsv'))[0]
        except IndexError:
            return []
        with tsv_path.open() as handle:
            header = handle.readline().strip().split('\t')
            return [{k: v for k, v in zip(header, l.strip().split('\t'))} for l in handle.readlines()]
