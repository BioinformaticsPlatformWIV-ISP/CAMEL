import os
from camel.app.components.blast.alignmentextraction import AlignmentExtraction
from camel.app.components.filesystemhelper import FileSystemHelper
from camel.app.io.tooliofile import ToolIOFile
from camel.app.tools.pipelines.sequence_typing.besthitextractor import BestHitExtractor
from camel.app.tools.tool import Tool


class AlignmentExtractor(Tool):
    """
    Tool that extracts alignments from TXT files generated by blastx or blastn.
    - Files containing the alignments should be specified alphabetically in the tool inputs ('TXT') with the DNA loci
    first, peptide loci second.
    - The hits have to be specified in the informs with the following format:
    {
        'locus_A': {'hit': BlastHit(), 'type': 'DNA', ...},
        'locus_B': {'hit': BlastHit(), 'type': 'peptide', ...}
    }
    """

    def __init__(self, camel):
        """
        Initializes this tool.
        :param camel: Camel instance
        """
        super(AlignmentExtractor, self).__init__('Sequence Typing: Alignment Extractor', '0.1', camel)

    def _execute_tool(self):
        """
        Runs this tool.
        :return: None
        """
        self._tool_outputs['TXT'] = []

        hits = self._input_informs['hits']
        for locus, alignments_file in zip(AlignmentExtractor.__get_hit_keys_ordered(hits), self._tool_inputs['TXT']):
            alignments = AlignmentExtraction.get_alignments(alignments_file.path)
            if hits[locus]['hit_type'] not in (BestHitExtractor.NO_HIT, BestHitExtractor.MULTIPLE_HITS):
                hit = hits[locus]['hit']
                key = AlignmentExtraction.get_key(hit.database_gene, hit.query)
                if key in alignments:
                    self._tool_outputs['TXT'].append(ToolIOFile(self.__save_alignment(locus, alignments[key])))
                else:
                    raise ValueError("No alignment found for: '{}'".format(key))

    def _check_input(self):
        """
        Checks if the required inputs are specified.
        :return: None
        """
        if 'TXT' not in self._tool_inputs:
            raise ValueError("No TXT input found.")
        if 'hits' not in self._input_informs:
            raise ValueError("No blast hits input found")
        if len(self._tool_inputs['TXT']) != len(self._input_informs['hits']):
            raise ValueError("Number of text alignments is not equal to the number of hits")
        super(AlignmentExtractor, self)._check_input()

    @staticmethod
    def __get_hit_keys_ordered(hits):
        """
        Returns the keys for the hits ordered according to type ('DNA' > 'Peptide') and name (alphabetical).
        :param hits: Dictionary of hits
        :return: Ordered keys
        """
        keys_dna = [key for key in hits if hits[key]['type'] == 'DNA']
        keys_peptide = [key for key in hits if hits[key]['type'] == 'peptide']
        return sorted(keys_dna) + sorted(keys_peptide)

    def __save_alignment(self, subject_name, alignment):
        """
        Saves the given alignment.
        :param alignment: Alignment
        :return: Filename of the saved alignment
        """
        filename = os.path.join(self._folder, '{}.txt'.format(FileSystemHelper.make_valid(subject_name)))
        with open(filename, 'w') as output_handle:
            output_handle.write(alignment)
        return filename
